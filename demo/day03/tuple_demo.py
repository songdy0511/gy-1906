# 列表和元组的区别
# 1. 元组中只有一个数据的时候，后边必须带一个逗号，列表不需要
a = (3,)
y = [3]
b = (2,7)
print(a)
print(b)
# 2. 元组中的数据不可修改
# a[0] = 5
# print(a)
# 元组合并
c = (4,5,6)
d = (7,8,9)
print(c+d)
# 截取
# 截取部分数据
e = (3,1,5,7,6,4,3,2,4)
print(e[1:3])
# 倒叙
print(e[::-1])
# 隔一个取一个
print(e[::2])

# 获取长度
print(len(e))
# 成员判断
if (10 in e):
    print("存在列表中")
else:
    print("不存在列表中")


# 查
# 根据下标查询某个元素
print(e[0])
print(e[2])
# 遍历（借助循环）
for i in e:
    print(i)


